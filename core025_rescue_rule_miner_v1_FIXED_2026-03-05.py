def load_events(event_paths: List[str]) -> pd.DataFrame:
    parts = []
    for p in event_paths:
        df = pd.read_csv(p)
        df["__source_file__"] = os.path.basename(p)
        parts.append(df)
    return pd.concat(parts, ignore_index=True)

def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--event", action="append", default=[], help="Path to per-event export CSV (can repeat).")
    ap.add_argument("--auto", action="store_true", help="Auto-load all core025_tier_per_event*.csv in the current folder.")
    ap.add_argument("--min_support", type=int, default=8)
    ap.add_argument("--max_rules", type=int, default=40)
    args = ap.parse_args()

    event_paths = list(args.event)
    if args.auto:
        event_paths.extend(sorted(glob.glob("core025_tier_per_event*.csv")))
    event_paths = [p for p in event_paths if os.path.exists(p)]

    if not event_paths:
        print("❌ No per-event CSVs found.\n"
              "Provide at least one with --event <file.csv> or put them in this folder and run --auto.\n"
              "Expected names include:\n"
              "  core025_tier_per_event_run_train_to__bins21_cap80_runf947fdfc.csv\n"
              "  core025_tier_per_event_run_train_to__rows2-76_cap50_rune9173474.csv")
        return 2

    df = load_events(event_paths)
    print(f"Loaded {len(df):,} rows from {len(event_paths)} per-event file(s).")

    # Identify key columns (robust guesses)
    seed_col = find_col(df, ["Seed", "seed", "seed4", "seed_4d", "StreamSeed", "stream_seed", "SeedStr"], required=True)
    true_col = find_col(df, ["TrueMember", "true_member", "WinningMember", "winner_member", "WinMember", "MemberTrue", "true"], required=True)
    top1_col = find_col(df, ["Top1Member", "top1_member", "Pick1Member", "Top1", "top1"], required=True)
    top2_col = find_col(df, ["Top2Member", "top2_member", "Pick2Member", "Top2", "top2"], required=True)
    top3_col = find_col(df, ["Top3Member", "top3_member", "Pick3Member", "Top3", "top3"], required=True)

    # Normalize
    out = df.copy()
    out["seed4"] = out[seed_col].map(coerce_seed4)
    out["true_member"] = out[true_col].map(coerce_member)
    out["top1_member"] = out[top1_col].map(coerce_member)
    out["top2_member"] = out[top2_col].map(coerce_member)
    out["top3_member"] = out[top3_col].map(coerce_member)

    # Drop rows we can't score
    before = len(out)
    out = out.dropna(subset=["seed4", "true_member", "top1_member", "top2_member", "top3_member"]).copy()
    print(f"Scorable rows: {len(out):,} (dropped {before-len(out):,} with missing seed/member picks).")

    # Derive features from seed (no app edits)
    out = derive_seed_features(out, "seed4")

    # Choose feature columns for mining
    num_cols = [
        "seed_sum_calc", "seed_spread_calc", "seed_absdiff_calc", "seed_sum_lastdigit_calc",
        "seed_even_ct_calc", "seed_ct025_calc", "seed_adj_absdiff_sum_calc", "seed_pairwise_absdiff_sum_calc",
    ]
    bool_cols = ["seed_has_9_calc"] + [c for c in out.columns if c.startswith("seed_has_d")] + [c for c in out.columns if c.startswith("seed_has_pair_")]

    # Baseline metrics overall / regimes
    def summarize(sub: pd.DataFrame, label: str):
        b = baseline_metrics(sub, "true_member", "top1_member", "top2_member", "top3_member")
        top3_true = int((sub["true_member"] == sub["top3_member"]).sum())
        top3_by_member = sub.loc[sub["true_member"] == sub["top3_member"], "true_member"].value_counts().to_dict()
        print(f"\n=== {label} ===")
        print(f"Events: {len(sub):,}")
        print(f"Top1: {b['Top1']*100:.1f}% | Top1+Top2: {b['Top1or2']*100:.1f}% | Top1+Top2+Top3: {b['Top1or2or3']*100:.1f}%")
        print(f"Top3-was-true: {top3_true:,} ({(top3_true/len(sub))*100:.1f}%) | breakdown: {top3_by_member}")

    summarize(out, "ALL seed regimes")
    summarize(out[out["seed_has_9_calc"]==1], "seed_has_9")
    summarize(out[out["seed_has_9_calc"]==0], "seed_no_9")

    # Mine rescue rules
    print("\nMining Top3→Top1 rescue rules (overall)...")
    rules_all = mine_top3_rescue_rules(out, "true_member", "top1_member", "top2_member", "top3_member", num_cols, bool_cols, max_rules=args.max_rules, min_support=args.min_support)
    print(f"Found {len(rules_all):,} candidate rescue rules (ALL).")

    print("\nMining Top3→Top1 rescue rules (seed_has_9)...")
    rules_9 = mine_top3_rescue_rules(out[out["seed_has_9_calc"]==1], "true_member", "top1_member", "top2_member", "top3_member", num_cols, bool_cols, max_rules=args.max_rules, min_support=max(4, args.min_support//2))
    print(f"Found {len(rules_9):,} candidate rescue rules (seed_has_9).")

    print("\nMining Top3→Top1 rescue rules (seed_no_9)...")
    rules_no9 = mine_top3_rescue_rules(out[out["seed_has_9_calc"]==0], "true_member", "top1_member", "top2_member", "top3_member", num_cols, bool_cols, max_rules=args.max_rules, min_support=args.min_support)
    print(f"Found {len(rules_no9):,} candidate rescue rules (seed_no_9).")

    # Member pockets
    pockets = {}
    for member in (25, 225, 255):
        pockets[member] = mine_member_pockets(out, "true_member", member, num_cols, bool_cols, max_rules=20, min_support=args.min_support)

    # Export
    ts = _dt.datetime.now().strftime("%Y%m%d_%H%M%S")
    def save(df_: pd.DataFrame, stem: str):
        if df_ is None or df_.empty:
            return None
        fn = f"core025_{stem}__{ts}.csv"
        df_.to_csv(fn, index=False)
        return fn

    fn_all = save(rules_all, "rescue_rules_ALL")
    fn_9 = save(rules_9, "rescue_rules_seed_has_9")
    fn_no9 = save(rules_no9, "rescue_rules_seed_no9")

    pocket_files = []
    for m, pdf in pockets.items():
        f = save(pdf, f"member_pockets_{m}")
        if f: pocket_files.append(f)

    # Display top rules to console
    def show(df_: pd.DataFrame, title: str, n: int = 15):
        if df_ is None or df_.empty:
            print(f"\n{title}: (none)")
            return
        cols = ["rule","support","precision_top3","recall_top3","Top1_gain","Top1_base","Top1_after"]
        keep = [c for c in cols if c in df_.columns]
        print(f"\n{title} (top {min(n,len(df_))})")
        print(df_[keep].head(n).to_string(index=False))

    show(rules_all, "TOP Rescue rules — ALL")
    show(rules_9, "TOP Rescue rules — seed_has_9")
    show(rules_no9, "TOP Rescue rules — seed_no_9")

    for member in (25,225,255):
        pdf = pockets[member]
        if pdf is None or pdf.empty:
            continue
        print(f"\nMember pockets — {member} (top 10)")
        print(pdf.head(10).to_string(index=False))

    print("\nExports:")
    for fn in [fn_all, fn_9, fn_no9, *pocket_files]:
        if fn:
            print(f" - {fn}")

    return 0


