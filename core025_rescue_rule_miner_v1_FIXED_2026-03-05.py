@dataclass(frozen=True)
class Predicate:
    col: str
    op: str
    val: float

    def apply(self, df: pd.DataFrame) -> pd.Series:
        if self.op == "==":
            return df[self.col] == self.val
        if self.op == "!=":
            return df[self.col] != self.val
        if self.op == ">=":
            return df[self.col] >= self.val
        if self.op == "<=":
            return df[self.col] <= self.val
        if self.op == ">":
            return df[self.col] > self.val
        if self.op == "<":
            return df[self.col] < self.val
        raise ValueError(self.op)

    def __str__(self) -> str:
        v = int(self.val) if float(self.val).is_integer() else self.val
        return f"{self.col} {self.op} {v}"

@dataclass(frozen=True)
class Rule:
    preds: Tuple[Predicate, ...]

    def apply(self, df: pd.DataFrame) -> pd.Series:
        m = pd.Series(True, index=df.index)
        for p in self.preds:
            m &= p.apply(df)
        return m

    def __str__(self) -> str:
        return " AND ".join(str(p) for p in self.preds)


