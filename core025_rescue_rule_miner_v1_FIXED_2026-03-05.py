def _norm(s: str) -> str:
    return re.sub(r"[^a-z0-9]+", "", str(s).lower())

def find_col(df: pd.DataFrame, candidates: List[str], required: bool = True) -> Optional[str]:
    cols = list(df.columns)
    norm_map = {_norm(c): c for c in cols}
    for cand in candidates:
        key = _norm(cand)
        if key in norm_map:
            return norm_map[key]
    # fuzzy contains
    for cand in candidates:
        key = _norm(cand)
        for n, c in norm_map.items():
            if key and key in n:
                return c
    if required:
        raise KeyError(f"Required column not found. Tried: {candidates}. Available columns: {cols[:60]}{'...' if len(cols)>60 else ''}")
    return None

def coerce_member(x) -> Optional[int]:
    """Coerce a member value to int in {25, 225, 255} if possible.

    Accepts inputs like: 25, "25", "0025", "0225", "0255", 225, "225", 255, "255".
    """
    if pd.isna(x):
        return None

    s = str(x).strip()
    parts = re.findall(r"\d+", s)
    if not parts:
        return None

    raw = parts[-1]        # last numeric group
    raw4 = raw.zfill(4)    # normalize for 4-digit member strings

    if raw4 in ("0025", "0225", "0255"):
        return int(raw4)   # int("0025") -> 25

    try:
        v = int(raw)
    except Exception:
        return None

    if v in (25, 225, 255):
        return v

    return None


def coerce_seed4(x) -> Optional[str]:
    if pd.isna(x):
        return None
    s = str(x).strip()
    m = re.findall(r"\d{4}", s)
    if m:
        return m[-1]
    # sometimes seed stored as 123 -> "0123"
    m2 = re.findall(r"\d+", s)
    if not m2:
        return None
    v = m2[-1]
    if len(v) <= 4:
        return v.zfill(4)
    return v[-4:]


