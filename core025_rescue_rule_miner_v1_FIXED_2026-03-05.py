PAIR_MIRROR = {"0":"5","1":"6","2":"7","3":"8","4":"9","5":"0","6":"1","7":"2","8":"3","9":"4"}

def seed_digits(seed4: str) -> List[int]:
    return [int(ch) for ch in seed4]

def derive_seed_features(df: pd.DataFrame, seed_col: str) -> pd.DataFrame:
    s = df[seed_col].map(coerce_seed4)
    df = df.copy()
    df["seed4"] = s
    d = df["seed4"].dropna().map(seed_digits)
    # preallocate
    df["seed_sum_calc"] = np.nan
    df["seed_spread_calc"] = np.nan
    df["seed_absdiff_calc"] = np.nan
    df["seed_sum_lastdigit_calc"] = np.nan
    df["seed_has_9_calc"] = np.nan
    df["seed_even_ct_calc"] = np.nan
    df["seed_ct025_calc"] = np.nan
    df["seed_adj_absdiff_sum_calc"] = np.nan
    df["seed_pairwise_absdiff_sum_calc"] = np.nan

    # compute vectorized-ish
    idx = d.index
    arr = np.array(d.tolist(), dtype=int)
    df.loc[idx, "seed_sum_calc"] = arr.sum(axis=1)
    df.loc[idx, "seed_spread_calc"] = arr.max(axis=1) - arr.min(axis=1)
    df.loc[idx, "seed_absdiff_calc"] = np.abs((arr[:,0] + arr[:,1]) - (arr[:,2] + arr[:,3]))
    df.loc[idx, "seed_sum_lastdigit_calc"] = (arr.sum(axis=1) % 10)
    df.loc[idx, "seed_has_9_calc"] = (arr == 9).any(axis=1).astype(int)
    df.loc[idx, "seed_even_ct_calc"] = (arr % 2 == 0).sum(axis=1)
    df.loc[idx, "seed_ct025_calc"] = np.isin(arr, [0,2,5]).sum(axis=1)
    df.loc[idx, "seed_adj_absdiff_sum_calc"] = (np.abs(arr[:,0]-arr[:,1]) + np.abs(arr[:,1]-arr[:,2]) + np.abs(arr[:,2]-arr[:,3]))
    # unordered pairwise absdiff sum
    pair_sums = []
    for row in arr:
        ps = 0
        for i,j in combinations(range(4), 2):
            ps += abs(int(row[i]) - int(row[j]))
        pair_sums.append(ps)
    df.loc[idx, "seed_pairwise_absdiff_sum_calc"] = pair_sums

    # digit presence flags
    for dig in range(10):
        df[f"seed_has_d{dig}"] = df["seed4"].fillna("").str.contains(str(dig)).astype(int)

    # unordered pair flags (00..99 but unordered unique)
    for a in range(10):
        for b in range(a, 10):
            name = f"seed_has_pair_{a}{b}"
            def _has_pair(seed: str, a=a, b=b):
                if not isinstance(seed, str) or len(seed) != 4:
                    return 0
                ct_a = seed.count(str(a))
                ct_b = seed.count(str(b))
                if a == b:
                    return 1 if ct_a >= 2 else 0
                return 1 if (ct_a >= 1 and ct_b >= 1) else 0
            df[name] = df["seed4"].map(_has_pair).astype(int)

    return df


